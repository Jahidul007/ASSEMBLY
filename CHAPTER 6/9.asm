.MODEL SMALL
.STACK 100H
.DATA
.CODE 


MAIN PROC
MOV CX,07Fh ;INITIALIZE NUMBER OF CHARACTER
MOV BL,0 

PRINT:
    MOV AH,2
    INC CX 
    CMP CX,0FFh  

    JA EXIT:
    MOV DX,CX
    INT 21H 
    
    MOV DX,20h ; GIVING SPACE
    INT 21H
    JMP GO  

GO:
    INC BL
    CMP BL,10 ; 10 CHAR PER LINE
    JE NL
    JMP PRINT 

NL:
    MOV AH,2
    MOV DL,0DH
    INT 21H
    MOV DL,0AH
    INT 21H 

MOV BL,0
JMP PRINT  

EXIT: